    private void selectdata() 
    {
        String kolom[]={"No. Struk","Nama Item","JumlahItem","HargaItem"};
        DefaultTableModel dtm = new DefaultTableModel(null, kolom);   
        String SQL = "SELECT * FROM tb_menu";
        ResultSet rs = KoneksiDB.executeQuery(SQL);
        try{
            while(rs.next()){
                String id = rs.getString(1);
                String item = rs.getString(2);
                String jumlah = rs.getString(3);
                String harga = rs.getString(4);
//                String total = rs.getString(5);
                    String data[] = {id,item,jumlah,harga};
                    dtm.addRow(data);
            }
        }   catch(SQLException ex){
                    Logger.getLogger(Pemesanan.class.getName()).log(Level.SEVERE,null, ex);
                    }
            tableData1.setModel(dtm);
    }

    private void SetWaktu() {
        ActionListener taskPerformer = new ActionListener(){
        public void actionPerformed(ActionEvent evt){
                String nol_jam = "";
                String nol_menit = "";
                String nol_detik = "";
                
                
                Date dt = new Date();
                int vh = dt.getHours();
                int vm = dt.getMinutes();
                int vs = dt.getSeconds();
                
                if (vh<=9){
                    nol_jam = "0";
                } if (vm <= 9) {
                    nol_menit = "0";
                } if (vs <= 9){
                    nol_detik = "0";
                }
               
                
                String h = nol_jam + Integer.toString(vh);
                String m = nol_menit + Integer.toString(vm);
                String s = nol_detik + Integer.toString(vs);
                wkt.setText(h+":"+m+":"+s);
            }
    };

   new Timer(100, taskPerformer).start();
    }


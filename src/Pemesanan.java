    private void totalhrgActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_totalhrgActionPerformed
        int jmlBaris = tableData1.getRowCount();
        int totalBiaya = 0;
        int jmlBarang, hargaBarang;
        TableModel tabelModel;
        tabelModel = tableData1.getModel();
        for (int i=0; i<jmlBaris; i++){
            jmlBarang = Integer.parseInt(tabelModel.getValueAt(i, 2).toString());
            hargaBarang = Integer.parseInt(tabelModel.getValueAt(i, 3).toString());
            totalBiaya = totalBiaya + (jmlBarang*hargaBarang);
        }
        if (totalBiaya > 200000) {
            int diskon = totalBiaya*10/100;
            hrgTotal.setText(String.valueOf(totalBiaya-diskon));
        }else
        hrgTotal.setText(String.valueOf(totalBiaya));
    }//GEN-LAST:event_totalhrgActionPerformed

